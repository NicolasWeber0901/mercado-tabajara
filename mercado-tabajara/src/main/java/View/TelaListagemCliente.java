        if (filtro == 0) {
            String searchText = tfPesquisar.getText();
            myTableSorter.setRowFilter(new MyRowFilter(searchText));
        } else if (filtro == 1) {
            String searchText = tfPesquisar.getText();
            myTableSorter.setRowFilter(new MyRowFilterNome(searchText));
        }
